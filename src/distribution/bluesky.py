# Post composition
# ---------------------------------------------------------------------------


def compose_post(*, social_hook: str | None, title: str) -> str:
    """Compose the visible TEXT of a Bluesky post.

    The text is the conversational hook only — the URL, title, and
    description live in the attached rich link card embed. This is the
    deliberate "modern" format: a human-sounding sentence, then the
    card preview, no hashtags, no naked URL.

    Falls back to the title if the hook is missing (legacy posts that
    were generated before social_hook existed; the backfill script
    should populate them but we degrade gracefully if not).
    """
    hook = (social_hook or "").strip()
    if hook:
        if len(hook) > _MAX_POST_CHARS:
            hook = hook[: _MAX_POST_CHARS - 1].rstrip()
            # Avoid mid-word ellipsis where possible.
            if " " in hook:
                hook = hook[: hook.rfind(" ")]
            hook = hook + "…"
        return hook

    # Fallback: just the title.
    fallback = (title or "").strip()
    if len(fallback) > _MAX_POST_CHARS:
        fallback = fallback[: _MAX_POST_CHARS - 1].rstrip() + "…"
    return fallback


# ---------------------------------------------------------------------------
# Module-level loader
# ---------------------------------------------------------------------------


def is_enabled() -> bool:
    return bool(settings.bluesky_handle.strip() and settings.bluesky_app_password.strip())


def get_client() -> BlueskyClient | None:
    if not is_enabled():
        return None
    return BlueskyClient(
        handle=settings.bluesky_handle.strip(),
        app_password=settings.bluesky_app_password.strip(),
    )
